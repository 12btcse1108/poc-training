package reference.classes;

public class Data {

	private Extras extras;
	private InputRecord input;

	private OutputRecord output;

	public Extras getExtras() {
		return extras;
	}

	public InputRecord getInput() {
		return input;
	}

	public OutputRecord getOutput() {
		return output;
	}

	public void setExtras(Extras extras) {
		this.extras = extras;
	}

	public void setInput(InputRecord input) {
		this.input = input;
	}

	public void setOutput(OutputRecord output) {
		this.output = output;
	}

}
